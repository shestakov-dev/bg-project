// Function to map years to Z coordinates
function mapYearToZ(year: number, minYear: number, maxYear: number): number {
	// Special case for Odyssey
	if (year === -750) {
		return -50;
	}

	// Map from [minYear, maxYear] to [-25, 50]
	return ((year - minYear) / (maxYear - minYear)) * 75 - 25;
}

